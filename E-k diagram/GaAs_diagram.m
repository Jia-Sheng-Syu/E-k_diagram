clear; clc; close all;
%Time Binding Parameters(GaAs)
P1=-6.72357;
P2=-3.97833;
P3=0.64095;
P4=2.87407;
P5=-6.90000;
P6=5.24000;
P7=4.32100;
P8=2.00000;
P9=5.50000;
P10=-0.33905;
P11=-1.75633;
P12=0.60000;
P13=0.96000;
P14=0.44445;
P15=1.12077;
P16=0.04520;
P17=0.09640;
P18=-0.04738;
P19=-0.06533;
P20=0.78000;
P21=-0.08000;
P22=0.00000;
P23=0.00000;
%k的路徑
nk=50;
L=[0.5,0.5,0.5];
Gamma=[0.0,0.0,0.0];
X=[1.0,0.0,0.0];
K=[0.75,0.75,0.0];
k_path=[
    linspace(L(1),Gamma(1),nk)',linspace(L(2),Gamma(2),nk)',linspace(L(3),Gamma(3),nk)';
    linspace(Gamma(1),X(1),nk)',linspace(Gamma(2),X(2),nk)',linspace(Gamma(3),X(3),nk)';
    linspace(X(1),K(1),nk)',linspace(X(2),K(2),nk)',linspace(X(3),K(3),nk)';
    linspace(K(1),Gamma(1),nk)',linspace(K(2),Gamma(2),nk)',linspace(K(3),Gamma(3),nk)'];
total_nk=size(k_path,1);
E_bands=zeros(total_nk,8);
for i=1:total_nk
    kx=k_path(i,1);
    ky=k_path(i,2);
    kz=k_path(i,3);
    C1=cos(pi*kx);
    C2=cos(pi*ky);
    C3=cos(pi*kz);
    S1=sin(pi*kx);
    S2=sin(pi*ky);
    S3=sin(pi*kz);
    cx=cos(pi*kx/2);
    cy=cos(pi*ky/2);
    cz=cos(pi*kz/2);
    sx=sin(pi*kx/2);
    sy=sin(pi*ky/2);
    sz=sin(pi*kz/2);
    g0=cx*cy*cz-1i*sx*sy*sz;
    g1=-cx*sy*sz+1i*sx*cy*cz;
    g2=-sx*cy*sz+1i*cx*sy*cz;
    g3=-sx*sy*cz+1i*cx*cy*sz;
    H=zeros(8,8);
    H(1,1)=P2+P19*(C1*C2+C2*C3+C3*C1);
    H(2,2)=P4+P15*C1*(C2+C3)+P11*C2*C3;
    H(3,3)=P4+P15*C2*(C1+C3)+P11*C1*C3;
    H(4,4)=P4+P15*C3*(C1+C2)+P11*C1*C2;
    H(5,5)=P1+P18*(C1*C2+C2*C3+C3*C1);
    H(6,6)=P3+P14*C1*(C2+C3)+P10*C2*C3;
    H(7,7)=P3+P14*C2*(C1+C3)+P10*C1*C3;
    H(8,8)=P3+P14*C3*(C1+C2)+P10*C1*C2;
    H(1,2)=-P21*S2*S3+1i*P17*S1*(C2+C3);
    H(1,3)=-P21*S1*S3+1i*P17*S2*(C1+C3);
    H(1,4)=-P21*S1*S2+1i*P17*S3*(C1+C2);
    H(2,3)=-P13*S1*S2-1i*P23*C3*(S1-S2);
    H(2,4)=-P13*S1*S3-1i*P23*C2*(S1-S3);
    H(3,4)=-P13*S2*S3-1i*P23*C1*(S2-S3);
    H(5,6)=P20*S2*S3+1i*P16*S1*(C2+C3);
    H(5,7)=P20*S1*S3+1i*P16*S2*(C1+C3);
    H(5,8)=P20*S1*S2+1i*P16*S3*(C1+C2);
    H(6,7)=-P12*S1*S2+1i*P22*C3*(S1-S2);
    H(6,8)=-P12*S1*S3+1i*P22*C2*(S1-S3);
    H(7,8)=-P12*S2*S3+1i*P22*C1*(S2-S3);
    H(1,5)=P5*g0;
    H(1,6)=P7*g1;
    H(1,7)=P7*g2;
    H(1,8)=P7*g3;
    H(2,5)=-P6*g1;
    H(2,6)=P8*g0;
    H(2,7)=P9*g3;
    H(2,8)=P9*g2;
    H(3,5)=-P6*g2;
    H(3,6)=P9*g3;
    H(3,7)=P8*g0;
    H(3,8)=P9*g1;
    H(4,5)=-P6*g3;
    H(4,6)=P9*g2;
    H(4,7)=P9*g1;
    H(4,8)=P8*g0;
    H=H+H'-diag(diag(H));
    eigenvalue=eig(H);
    E_bands(i,:)=sort(real(eigenvalue));
end
figure; hold on;
plot(1:total_nk,E_bands,'LineWidth',1.5);
xticks([1,nk,2*nk,3*nk,4*nk]);
xticklabels({'L','\Gamma','X', 'K','\Gamma'});
ylabel('Energy(eV)'); 
title('GaAs Band Structure');
grid on; 
box on; 
xlim([1, total_nk]);